#Mostrar el valor del iva del 21% de un producto con un valor de 100 y su precio final

iva = 21/100    #Aqui hacemos la division para el porcentaje que queremos en este caso

precio = 100    #Ponemos el precio del producto que compraremos

Valor_iva = precio * iva #Con esta operacion obtenemos el valor del IVA

Precio_final = precio + Valor_iva  #Sumamos nuestro precio y le sumamos el resultado de nuestra operacion para el IVA

print ("El valor del IVA es", Valor_iva)
print ("El costo total es de", Precio_final)

